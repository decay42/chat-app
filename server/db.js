; (async () => {
  const connector = mongoose.connect(process.env.MONGO_DB_CONNECTION_STRING)
  const username = process.argv[2].split('=')[1]

  let user = await connector.then(async () => {
    return findUser(username)
  })

  if (!user) {
    user = await createUser(username)
  }

  console.log(user)
  process.exit(0)
})()